        public static string 获取昵称()
        {
            string 昵称 = "";
            if (!私聊 && 群聊目标 != null)
            {
                GroupMember 群友 = 娶群友(群聊目标.FromQQ);
                昵称 = string.IsNullOrWhiteSpace(群友.Card)//取群名片
                    ? 群友.Nick : 群友.Card;//取QQ昵称
                if (实体.ContainsKey(群聊目标.FromQQ.ToString()))//取玩家设置昵称
                {
                    if (实体[群聊目标.FromQQ.ToString()].ContainsKey("昵称"))
                    {
                        昵称 = 实体[群聊目标.FromQQ.ToString()]["昵称"];
                    }
                }
            }
            else
            {
                QQInfo 好友 = Common.CqApi.GetQQInfo(私聊目标.FromQQ);
                昵称 = 好友.Nick;
            }
            return 昵称 + "的";
        }

        public static string 读取(string 获取语句)
        {
            string 属性补全 = "值";
            if (获取语句.Contains("、"))
            {
                List<string> 获取集合 = 获取语句.Split('、').ToList();
                if (获取集合[获取集合.Count - 1].Contains("的"))
                {
                    属性补全 = 获取集合[获取集合.Count - 1].Substring(获取集合[获取集合.Count - 1].IndexOf("的") + 1);
                }
                string 获取返回值 = "";
                foreach (var item in 获取集合)
                {
                    if (!item.Contains("的"))
                    {
                        获取返回值 += 读取组件(item + "的" + 属性补全) + "、";
                    }
                    else
                    {
                        获取返回值 += 读取组件(item) + "、";
                    }
                }
                return 获取返回值.TrimEnd('、');
            }
            if (!获取语句.Contains("的"))
            {
                获取语句 += "的值";
            }
            return 读取组件(获取语句);
        }

        public static void 写入实体(string 实体名, string 组件名, string 组件值)
        {
            if (!实体.ContainsKey(实体名))//如果没有这个实体就创建实体，并添加对应的组件
            {
                实体.Add(实体名, new Dictionary<string, string>());
                实体[实体名].Add(组件名, 组件值);
            }
            else
            {
                if (!实体[实体名].ContainsKey(组件名))
                {
                    实体[实体名].Add(组件名, 组件值);
                }
                else
                {
                    实体[实体名][组件名] = 组件值;
                }
            }
        }

